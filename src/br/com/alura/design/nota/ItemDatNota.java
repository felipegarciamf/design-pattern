package br.com.alura.design.nota;

public class ItemDatNota {

	private String nome;
	private double valor;

	public ItemDatNota(String nome, double valor) {
		this.nome = nome;
		this.valor = valor;
	}
	
	public String getNome() {
		return nome;
	}

	public double getValor() {
		return valor;
	}

	
	
}
